package com.ubivismedia.aidungeon.quests;

/**
 * Enum representing the different types of quests
 */
public enum QuestType {
    /**
     * Kill a specific entity or number of entities
     */
    KILL,
    
    /**
     * Collect a specific item or number of items
     */
    COLLECT,
    
    /**
     * Explore or discover specific locations
     */
    EXPLORE
}
